#include "main.h"

/**
 * print_diagonal - Draws a diagonal line of n '\' characters, followed by a new line.
 * @n: Number of '\' to print.
 */
void print_diagonal(int n)
{
	int i, j;

	if (n <= 0)
	{
		_putchar('\n');
		return;
	}
	for (i = 0; i < n; i++)
	{
		for (j = 0; j < i; j++)
			_putchar(' ');
		_putchar('\\');
		_putchar('\n');
	}
}
